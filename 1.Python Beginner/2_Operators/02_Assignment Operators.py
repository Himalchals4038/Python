# Values of variables can be changed with *= -= += /= operators.
# These operators can be stacked one upon other and the final result will come after evaluating all the functions.  

a = 2884
a += 45
print (a)

b = 9578
b -= 4577
print (b)

c = 458
c *= 3
print(c)

d = 3897
d /= 3
print(d)

e = 40
e += 60
e -= 20
e *= 3
e /= 6
print(e)
# Comparison operators are always used to compare between 2 or more numbers.
# The outputs of comparison are always true or false.

a = (91 > 91)
print(a)
b = (89 > 56)
print(b)
c = (34 < 9)
print(c)
d = (50 >= 50)
print(d)
e = (12 < 12)
print(e)
f = (19 <= 19)
print(f)
g = (14 == 14)
print(g)
h = (12 != 16)
print(h)
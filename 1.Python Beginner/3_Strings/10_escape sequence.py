# \t stands for tab i.e. shift the line by a tab distance. 

a = "Freundlich is a very good student. \nHe\t is very good in Maths and Science."
print(a)

b = "Mother Diana bakes choco\\late cakes on Chri\stmas for everyone."
print(b)

'''Conditionals are set of conditions specified by coder. If a value satisfies a specific condition, it will carry out the function related to that condition.'''

a = input("Enter the value of a: ")
a = int(a)
if (a>40) :
    print("The number is greater than 40")
elif (a<40) :
    print("The number is lesser than 40")
else :
    print("The number is equal to 40")


